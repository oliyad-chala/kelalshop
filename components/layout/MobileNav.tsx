export function MobileNav({ user }: MobileNavProps) {
  const pathname = usePathname()
  if (!user) return null

  const isShopper = user.role === 'shopper'
  const tabs = isShopper ? shopperTabs : buyerTabs

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-50 bg-white border-t border-slate-100 shadow-[0_-4px_24px_rgba(0,0,0,0.06)] safe-area-bottom">
      <div className="flex items-stretch justify-around h-16">
        {tabs.map((tab) => {
          const isActive =
            pathname === tab.href ||
            (tab.href !== '/dashboard' && pathname.startsWith(tab.href + '/'))
          return (
            <Link
              key={tab.href}
              href={tab.href}
              className={clsx(
                'flex flex-col items-center justify-center gap-0.5 flex-1 text-[10px] font-semibold transition-colors relative',
                isActive
                  ? 'text-amber-500'
                  : 'text-slate-400 hover:text-slate-600'
              )}
            >
              {isActive && (
                <span className="absolute top-0 left-1/2 -translate-x-1/2 w-8 h-0.5 bg-amber-500 rounded-full" />
              )}
              <span className={clsx('transition-transform', isActive && 'scale-110')}>
                {tab.icon}
              </span>
              <span>{tab.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
